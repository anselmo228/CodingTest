SELECT COUNT(*) AS FISH_COUNT, 
MAX(CASE WHEN LENGTH IS NULL THEN 10 ELSE LENGTH END)
AS MAX_LENGTH, FISH_TYPE
FROM FISH_INFO
WHERE FISH_TYPE 
IN
(SELECT FISH_TYPE FROM FISH_INFO
 WHERE LENGTH >= 33 )
GROUP BY FISH_TYPE
HAVING AVG(CASE WHEN LENGTH IS NULL THEN 10 ELSE LENGTH END) >= 33
ORDER BY FISH_TYPE